	void EditorCamera::Update(const float _delta) {
		ImGui::Begin("EditorCamera");

		float delta = _delta;
		ImGui::DragFloat("delta", &delta);
		ImGui::End();
	}

	//================================================================================================================================
	//================================================================================================================================
	void EditorCamera::Stop() {
		std::cout << "Stop" << std::endl;
	}
