#include "render/fanUIMeshManager.hpp"

#include "render/fanUIMesh.hpp"

namespace fan
{
	//================================================================================================================================
	//================================================================================================================================
	void UIMeshManager::Init( Device& _device )
	{
		m_device = &_device;
	}

	//================================================================================================================================
	//================================================================================================================================
	void UIMeshManager::Clear()
	{
		while ( !m_meshList.empty() ) 
		{ 
			delete m_meshList[ m_meshList.size() - 1 ];
			m_meshList.pop_back();
		}
	}
}