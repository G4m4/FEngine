#include "network/fanPacket.hpp"

namespace fan
{	

	//================================================================================================================================
	//================================================================================================================================
	Packet::Packet( const PacketTag _tag )
	{
		tag = _tag;
		m_packet << _tag;
	}

	//================================================================================================================================
	//================================================================================================================================
	PacketType Packet::ReadType()
	{
		sf::Uint16 intType;
		m_packet >> intType;
		return PacketType( intType );
	}

	//================================================================================================================================
	//================================================================================================================================
	void Packet::Clear()
	{
		m_packet.clear(); 
		tag = 0; 
		onlyContainsAck = false; 
		onFail.Clear();
		onSuccess.Clear();
	}
}